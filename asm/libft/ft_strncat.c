#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t count)
{
	char *p;

	p = s1;
	while (*p)
		p++;
	while (*s2 && count--)
		*(p++) = *(s2++);
	*(p) = '\0';
	return (s1);
}
